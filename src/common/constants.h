#ifndef CONSTANTS_H
#define CONSTANTS_H

/** @brief π (pi) as a 32-bit floating-point constant. */
#define PI_F32 3.14159265358979323846f

/** @brief π (pi) as a 64-bit floating-point constant. */
#define PI_F64 3.14159265358979323846

/** @brief Euler's number (e) as a 32-bit floating-point constant. */
#define E_F32 2.71828182845904523536f

/** @brief Euler's number (e) as a 64-bit floating-point constant. */
#define E_F64 2.71828182845904523536

/** @brief 2π as a 32-bit floating-point constant. */
#define TAU_F32 6.28318530717958647692f

/** @brief 2π as a 64-bit floating-point constant. */
#define TAU_F64 6.28318530717958647692

/** @brief π/2 as a 32-bit floating-point constant. */
#define HALF_PI_F32 1.57079632679489661923f

/** @brief π/2 as a 64-bit floating-point constant. */
#define HALF_PI_F64 1.57079632679489661923

#endif
